private void cargarUsuariosEliminados() {
  DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0); // Limpiar tabla
    
    UserCRUD userCRUD = new UserCRUD();
    try (ResultSet rs = userCRUD.obtenerUsuariosEliminados()) {
        while (rs != null && rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("nombre"),
                rs.getString("correo"),
                rs.getString("contraseña"),
                rs.getTimestamp("fecha_eliminacion")
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar usuarios eliminados: " + e.getMessage(), 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
}

