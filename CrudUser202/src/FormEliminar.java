    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
 String idUsuario = txtIDusuario.getText();
    if (idUsuario.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un valor", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int id = Integer.parseInt(idUsuario);
        UserCRUD userCRUD = new UserCRUD();
        try (ResultSet rs = userCRUD.obtenerUsuarioPorId(id)) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); 

            if (rs != null && rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("ID"),
                    rs.getString("Nombre"),
                    rs.getString("Correo"),
                    rs.getString("Contraseña")
                });
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró el usuario", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID debe ser un número", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException e) {
        System.out.println("Error al buscar id del usuario" + e.getMessage());
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         String idUsuario = txtIDusuario.getText().trim();

    if (idUsuario.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un ID de usuario válido", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(this, "¿Esta seguro de eliminar el usuario con ID " + idUsuario + "?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
    
    if (confirmacion == JOptionPane.YES_OPTION) {
        try {
            int id = Integer.parseInt(idUsuario);
            UserCRUD userCRUD = new UserCRUD();
            
            if (userCRUD.moverAEliminados(id)) {
               JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente y guardado en registros.", "Éxito", JOptionPane.INFORMATION_MESSAGE );
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0); 
            } else {
                JOptionPane.showMessageDialog(this," No se encontró el usuario con ID: " + idUsuario, "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID debe ser un número", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
     
    }//GEN-LAST:event_btnEliminarActionPerformed

